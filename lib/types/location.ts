// Helper function to get continent from country code
export const getContinentFromCountryCode = (countryCode: string): string => {
  const continent = Object.entries(CONTINENT_MAP).find(([_, countries]) =>
    countries.includes(countryCode)
  )?.[0];

  if (!continent) return "unknown";

  // Convert to required format
  switch (continent) {
    case "North America":
      return "north_america";
    case "South America":
      return "south_america";
    default:
      return continent.toLowerCase();
  }
};
